set -e

GREEN='\033[0;32m'
YELLOW='\033[1;33m'
NC='\033[0m'

echo -e "${GREEN}ğŸš€ Starting deployment...${NC}"

# Load environment
source .env

# Stop existing services
echo -e "${YELLOW}ğŸ›‘ Stopping existing services...${NC}"
docker-compose down || true

# Build and start without nginx first
echo -e "${YELLOW}ğŸ”¨ Building and starting core services...${NC}"
docker-compose up --build -d web db redis

# Wait for services
echo -e "${YELLOW}â³ Waiting for services to be ready...${NC}"
sleep 30

# Set up SSL if needed
echo -e "${YELLOW}ğŸ”’ Setting up SSL...${NC}"
./init-ssl.sh

# Start all services
echo -e "${YELLOW}ğŸŒ Starting all services with SSL...${NC}"
docker-compose up -d

# Final health check
echo -e "${YELLOW}ğŸ¥ Performing health checks...${NC}"
sleep 20

if curl -f -k https://$BACKEND_DOMAIN/health/status/ > /dev/null 2>&1; then
    echo -e "${GREEN}âœ… Deployment successful!${NC}"
    echo -e "${GREEN}ğŸŒ Your API is running at: https://$BACKEND_DOMAIN${NC}"
    echo -e "${GREEN}ğŸ“Š Admin panel: https://$BACKEND_DOMAIN/admin/${NC}"
    echo -e "${GREEN}ğŸ”Œ API docs: https://$BACKEND_DOMAIN/api/v1/${NC}"
else
    echo -e "${YELLOW}âš ï¸  SSL setup in progress. Check in a few minutes.${NC}"
    echo -e "${GREEN}ğŸŒ Your API is running at: http://$BACKEND_DOMAIN${NC}"
fi

echo
echo -e "${YELLOW}ğŸ“‹ Useful commands:${NC}"
echo "  â€¢ View logs: docker-compose logs -f"
echo "  â€¢ Stop: docker-compose down"
echo "  â€¢ Restart: docker-compose restart"
EOF

chmod +x deploy.sh

echo -e "${GREEN}âœ… Setup complete!${NC}"
echo
echo -e "${YELLOW}ğŸ“‹ Summary:${NC}"
echo "â€¢ Frontend domain: $FRONTEND_DOMAIN"
echo "â€¢ Backend domain: $BACKEND_DOMAIN"
echo "â€¢ SSL email: $ADMIN_EMAIL"
echo "â€¢ Environment file: .env (created)"
echo "â€¢ Docker config: docker-compose.yml (updated)"
echo "â€¢ Nginx config: nginx.prod.conf (created)"
echo
echo -e "${GREEN}ğŸš€ To deploy, run:${NC}"
echo -e "${BLUE}  ./deploy.sh${NC}"
echo
echo -e "${YELLOW}âš ï¸  Make sure your DNS points:${NC}"
echo "â€¢ $BACKEND_DOMAIN â†’ this server IP"
echo "â€¢ $FRONTEND_DOMAIN â†’ your frontend server IP"